"""
Три цветовода выращивают розы. Определить, какие из известных сортов роз «Анжелика»,
«Виктория», «Гагарин», «Катарина», «Юбилейная», «Южная» имеются у каждого
цветоводы, есть хотя бы у одного из цветоводов и каких нет ни у одного из цветоводов.
"""

grower_a = {'Юбилейная', 'Виктория', 'Катарина'}
grower_b = {'Виктория', 'Гагарин', 'Юбилейная'}
grower_c = {'Катарина', 'Юбилейная', 'Южная'}

# Универсальное множество всех возможных сортов
all_varieties = {'Анжелика', 'Виктория', 'Гагарин', 'Катарина', 'Юбилейная', 'Южная'}

try:
    # 1. Сорта, которые есть у каждого цветовода (пересечение)
    common_varieties = grower_a & grower_b & grower_c
    print("Сорта, которые есть у каждого цветовода:", common_varieties)

    # 2. Сорта, которые есть хотя бы у одного цветовода (объединение)
    all_grown_varieties = grower_a | grower_b | grower_c
    print("Сорта, которые есть хотя бы у одного цветовода:", all_grown_varieties)

    # 3. Сорта, которых нет ни у одного цветовода (дополнение)
    missing_varieties = all_varieties - all_grown_varieties
    print("Сорта, которых нет ни у одного цветовода:", missing_varieties)

except TypeError:
    print("Ошибка: Все входные данные должны быть множествами")