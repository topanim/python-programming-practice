"""
В последовательности на n целых чисел умножить все элементы на первый
элемент.
"""

# Умножение всех элементов последовательности на первый элемент
try:
    seq = [5, 2, -3, 4, 0, 7]  # Пример последовательности
    print(f"Исходная последовательность: {seq}")
    if seq:  # Проверяем, что последовательность не пуста
        result = [x * seq[0] for x in seq]
        print(f"Результирующая последовательность: {result}")
    else:
        print("Ошибка: Последовательность пуста")
except TypeError:
    print("Ошибка: Элементы должны быть числами")